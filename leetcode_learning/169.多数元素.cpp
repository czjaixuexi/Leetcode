// @lc code=start
class Solution {
public:
    int majorityElement(vector<int>& nums) {
        // 投票法，最后剩下的一定是众数
        int candidate = -1;
        int count = 0;
        for (int num : nums) {
            if (num == candidate)
                ++count;
            else if (--count < 0) {
                candidate = num;
                count = 1;
            }
        }
        return candidate;
    }
};
// @lc code=end

